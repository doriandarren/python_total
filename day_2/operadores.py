x = 6
y = 2
z = 7


print(f"{x} + {y} = {x+y}")
print(f"{x} - {y} = {x-y}")
print(f"{x} * {y} = {x*y}")
print(f"{x} / {y} = {x/y}")

print(f"{z} redondeo hacia abajo {y} = {z//y}") ## redondeo hacia abajo
print(f"{z} resto {y} = {z%y}")  ## resto
print(f"{x} elevado {y} = {x**y}")  ## potencia 2
print(f"{x} elevado {3} = {x**3}")  ## potencia 3
print(f"{x} raiz {x} = {x**0.5}")  ## raiz cuadrada